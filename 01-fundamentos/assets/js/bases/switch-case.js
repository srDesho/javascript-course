// Switch case, es una alternativa al los else if anidados.
console.log('SWITCH CASE');
let dia = 5;

switch (dia) {
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lúnes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 3:
        console.log('Miércoles');
        break;
    case 4:
        console.log('Jueves');
        break;
    case 5:
        console.log('Viernes');
        break;
    case 6:
        console.log('Sábado');
        break;
    default:
        break;
}